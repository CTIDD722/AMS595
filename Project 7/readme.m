This project implements each subproblem using a three-file structure (main.cpp + Qi.h + Qi.cpp).  
For each question, the core algorithm is implemented in Qi.cpp, and each source file includes a header comment at the top.

How to run:
- Compile and run main.cpp in any standard C++ environment (e.g., CLion, Visual Studio).
- No additional steps are required beyond building the project and executing the generated program.

